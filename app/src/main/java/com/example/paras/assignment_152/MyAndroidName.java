package com.example.paras.assignment_152;

// custom class created to store the android version name and return the data when called.
public class MyAndroidName {
    private String androidName;

    // constructor called which takes an argument of string type.
    public MyAndroidName(String androidName) {
        this.androidName = androidName;
    }

    // getter that returns a string.
    public String getAndroidName() {
        return androidName;
    }
}
